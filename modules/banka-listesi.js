export const LEGACY_ALIAS = {
    'AKBANK': 'AKBANK T.A.Ş.',
    'VAKIFBANK': 'TÜRKİYE VAKIFLAR BANKASI T.A.O.',
    'ZİRAAT BANKASI': 'T.C. ZİRAAT BANKASI A.Ş.',
};

/**
 * Normalizes a bank name string by trimming, removing extra spaces, and uppercasing.
 */
export const normalizeBankaAdi = (name) => {
    if (!name || typeof name !== 'string') return '';
    // We replace multiple spaces with a single space to allow matching "AK BANK" -> "AK BANK" etc. if needed
    // However, the user asked for "akbank t.a.ş." (noktasız) to match "AKBANK T.A.Ş."
    // Let's make the normalizer robust.
    return name
        .trim()
        .replace(/\s+/g, ' ')
        .toUpperCase()
        .replace('I', 'I') // handle turkish I/i issues if necessary? toUpperCase usually does it ok.
        .replace('İ', 'İ');
};

/**
 * Normalizes heavily to create a match-friendly string: removes all punctuation and spaces.
 * e.g. "AKBANK T.A.Ş." -> "AKBANKTAŞ"
 * "ak bank" -> "AKBANK"
 */
const getStrippedName = (name) => {
    return (name || '')
        .toUpperCase()
        .replace(/[\s\.,\-']/g, '') // remove spaces, dots, commas, hyphens
        .replace(/İ/g, 'I');
};

// Create a precomputed map of stripped canonical names to their exact forms.
const canonicalMap = {};
BANKA_LISTESI.forEach(b => {
    canonicalMap[getStrippedName(b.banka_adi)] = b.banka_adi;
});

// Also add legacy aliases to the map
Object.keys(LEGACY_ALIAS).forEach(alias => {
    canonicalMap[getStrippedName(alias)] = LEGACY_ALIAS[alias];
});

/**
 * Finds the canonical bank name based on strict matching rules (with punctuation/space normalization).
 */
export const getCanonicalBankaAdi = (inputName) => {
    if (!inputName || typeof inputName !== 'string') return null;
    
    // First, check if the input is directly in LEGACY_ALIAS after basic normalization
    const basicNorm = inputName.trim().toUpperCase();
    if (LEGACY_ALIAS[basicNorm]) {
        return LEGACY_ALIAS[basicNorm];
    }
    
    // Strip everything and search in the map
    const stripped = getStrippedName(inputName);
    return canonicalMap[stripped] || null;
};
